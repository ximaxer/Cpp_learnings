
import java.util.ArrayList;

public class Docent extends Person{
    protected ArrayList<Project> projects = new ArrayList<Project>();
    
    //========================================================================//
    //========================================================================//
    //============================             ===============================//
    //============================  variables  ===============================//
    //============================             ===============================//
    //========================================================================//
    //========================================================================// 
    private int numMecha;
    private String area;
    //========================================================================//
    //========================================================================//
    //===========================                =============================//
    //===========================  Constructors  =============================//
    //===========================                =============================//
    //========================================================================//
    //========================================================================//
    public Docent() {
        this.numMecha=0;
        this.area=null;
    }
    
    public Docent(String name, String email, int numMecha, String area) {
        super.name = name;
        super.email = email;
        this.numMecha=numMecha;
        this.area=area;
        cost();
    }
    
    //========================================================================//
    //========================================================================//
    //=============================           ================================//
    //=============================  methods  ================================//
    //=============================           ================================//
    //========================================================================//
    //========================================================================//
    public int getNumMecha() {
        return numMecha;
    }

    public void setNumMecha(int numMecha) {
        this.numMecha = numMecha;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }
    
    @Override
    public void cost(){
        super.Salary=0.00;
    }
}

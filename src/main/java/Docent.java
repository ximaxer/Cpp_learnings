public class Docent extends Person{

    

    
    //========================================================================//
    //========================================================================//
    //============================             ===============================//
    //============================  variables  ===============================//
    //============================             ===============================//
    //========================================================================//
    //========================================================================// 
    private int numMecha;
    private String area;
    private double Income;
    //========================================================================//
    //========================================================================//
    //===========================                =============================//
    //===========================  Constructors  =============================//
    //===========================                =============================//
    //========================================================================//
    //========================================================================//
    public Docent() {
        this.numMecha=0;
        this.area=null;
    }
    
    public Docent(int numMecha, String area, double Income) {
        this.numMecha=numMecha;
        this.area=area;
        this.Income=Income;
        cost();
    }
    
    //========================================================================//
    //========================================================================//
    //=============================           ================================//
    //=============================  methods  ================================//
    //=============================           ================================//
    //========================================================================//
    //========================================================================//
    public int getNumMecha() {
        return numMecha;
    }

    public void setNumMecha(int numMecha) {
        this.numMecha = numMecha;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }
    
    @Override
    public void cost(){
        super.Salary=this.Income;
    }
}
